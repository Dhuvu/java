public class JavaOperations {

    public static void main(String[] args) {

        int A=40;
        int B=60;
        System.out.println("java operation examples");
        System.out.println("sum of A&B="+((A+B)));      //100
        System.out.println("sub of A&B="+((A-B)));   //-20
        System.out.println("div of A&B="+((A/B)));    //0.666666
        System.out.println("mul of A&B="+((A*B)));    //2400
        System.out.println("PostIncrement of A="+((A++)));  //40
        System.out.println( "PostIncrement of B="+((B++)));  //60
        System.out.println("PostDecrement of A="+((A--))); //41
        System.out.println("PostDecrement of B="+((B--)));  //61
        System.out.println("PreIncrement of A="+((++A)));  //41
        System.out.println( "PreIncrement of B="+((++B)));  //61
        System.out.println("PreDecrement of A="+((--A)));  //40
        System.out.println("PreDecrement of B="+((--B))); //60
    }
}
